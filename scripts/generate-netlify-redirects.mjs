#!/usr/bin/env node
// Write dist/pwa/_redirects after the quasar build.
//
// Why not netlify.toml: [[context.<name>.redirects]] blocks are silently
// ignored — Netlify only honors context scoping for build settings and
// environment, not redirects — so the per-context /webcam proxy rules there
// never fired in production. A _redirects file in the publish directory IS
// honored (and takes precedence over netlify.toml rules), and generating it
// at build time lets the bucket follow the same PRODUCTION switch the client
// bundle uses (src/boot/firebase.js), so the proxy always points at the
// bucket the app's data references.
//
// The /webcam/* proxy serves webcam frames same-origin — required by the
// browser-side lineup classifier (useLineupClassifier.js): the bucket sends
// no CORS headers, so cross-origin canvas pixel reads would be blocked.

import { writeFileSync, existsSync } from 'node:fs'
import { join, dirname } from 'node:path'
import { fileURLToPath } from 'node:url'

const dist = join(dirname(fileURLToPath(import.meta.url)), '..', 'dist', 'pwa')
if (!existsSync(dist)) {
  console.error(`generate-netlify-redirects: ${dist} not found — run after quasar build`)
  process.exit(1)
}

const bucket =
  process.env.PRODUCTION === 'true'
    ? 'bowen-ferry.firebasestorage.app'
    : 'bowen-ferry-staging.firebasestorage.app'

const rules = `# Generated by scripts/generate-netlify-redirects.mjs — do not edit.
/webcam/*  https://storage.googleapis.com/${bucket}/webcams/:splat  200
`

writeFileSync(join(dist, '_redirects'), rules)
console.log(`_redirects written (webcam proxy → ${bucket})`)
